/**
 *  按字节 循环左移一位
 *  即把[a0, a1, a2, a3]变成[a1, a2, a3, a0]
 */
inline std::uint32_t RotWord(std::uint32_t x) { return (x >> 8 | x << 24); }

/**
 *  密钥扩展函数 - 对128位密钥进行扩展得到 w[4*(Nr+1)]
 */
inline void KeyExpansion(const char* key, std::uint32_t* w, int Nk, int KeyLen, int KeyExt) {
	// w[]的前4个就是输入的key
	memcpy(w, key, KeyLen);
	for (int i = Nk; i < KeyExt; ++i)
		w[i] = (i % Nk == 0) ? w[i - Nk] ^ SubWord(RotWord(w[i - 1])) ^ Rcon[i / Nk] :
		(Nk == 8 && ((i & 0x3) == 0)) ? w[i - Nk] ^ SubWord(w[i - 1]) :
		w[i - Nk] ^ w[i - 1];
}

static constexpr size_t BLOCK_SIZE = aes_base::BLOCK_SIZE;
void encrypt(std::uint8_t* out, const std::uint8_t* in, const std::uint8_t* key, int Nr)
{
	std::uint8_t state[BLOCK_SIZE];
	Det(state, in);

	AddRoundKey(state, key);
	for (int round = 1; round < Nr; ++round)
	{
		SubBytes((std::uint32_t*)state);
		ShiftRows((std::uint32_t*)state);
		MixColumns(state);
		AddRoundKey(state, key += BLOCK_SIZE);
	}

	SubBytes((std::uint32_t*)state);
	ShiftRows((std::uint32_t*)state);
	AddRoundKey(state, key += BLOCK_SIZE);

	Det(out, state);
}

void decrypt(std::uint8_t* out, const std::uint8_t* in, const std::uint8_t* key, int Nr)
{
	std::uint8_t state[16];
	Det(state, in);

	AddRoundKey(state, key += BLOCK_SIZE * Nr);

	for (int round = 1; round < Nr; ++round)
	{
		InvShiftRows((std::uint32_t*)state);
		InvSubBytes((std::uint32_t*)state);
		AddRoundKey(state, key -= BLOCK_SIZE);
		InvMixColumns((std::uint8_t*)state);
	}

	InvShiftRows((std::uint32_t*)state);
	InvSubBytes((std::uint32_t*)state);
	AddRoundKey(state, key -= BLOCK_SIZE);

	Det(out, state);
}
