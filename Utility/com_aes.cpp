//  伽罗华域中的乘法
uint8_t GMul(uint8_t u, uint8_t v) {
	uint8_t p = 0,f;
	for (int i = 0; i < 8; ++i) {
		if (u & 0x01) p ^= v;
		f = v & 0x80;
		v <<= 1;
		if (f) v ^= 0x1B;  /* P(x) = x^8 + x^4 + x^3 + x + 1 */
		u >>= 1;
	}
	return p;
}

std::uint8_t GFMul(std::uint8_t u, std::uint8_t v) {
	std::uint8_t p = 0;
	for (int i = 0; i < 8; ++i) {
		if (u & 0x01) p ^= v;
		v = Mul_02[v]; //调用GMul(2，v)
		u >>= 1;
	}
	return p;
}
/**
 *  对输入word中的每一个字节进行S-盒变换
 */
#define SUB_WORD(Box,x)\
	(Box[(x & 0x000000F0) >> 0x04][(x & 0x0000000F) >> 0x00] << 0x00)\
	| (Box[(x & 0x0000F000) >> 0x0C][(x & 0x00000F00) >> 0x08] << 0x08)\
	| (Box[(x & 0x00F00000) >> 0x14][(x & 0x000F0000) >> 0x10] << 0x10)\
	| (Box[(x & 0xF0000000) >> 0x1C][(x & 0x0F000000) >> 0x18] << 0x18)
inline std::uint32_t SubWord(std::uint32_t x) { return SUB_WORD(S_Box, x); }
inline void SubBytes(std::uint32_t* mtx) { for (int i = 0; i < 4; ++i, ++mtx) *mtx = SUB_WORD(S_Box, *mtx); }
inline void InvSubBytes(std::uint32_t* mtx) { for (int i = 0; i < 4; ++i, ++mtx) *mtx = SUB_WORD(Inv_S_Box, *mtx); }

inline void ShiftRows(std::uint32_t* mtx) {
	++mtx;
	*mtx = (*mtx >> 8) | (*mtx << 24);
	++mtx;
	*mtx = (*mtx >> 16) | (*mtx << 16);
	++mtx;
	*mtx = (*mtx >> 24) | (*mtx << 8);
}

inline void InvShiftRows(std::uint32_t* mtx) {
	++mtx;
	*mtx = (*mtx << 8) | (*mtx >> 24);
	++mtx;
	*mtx = (*mtx << 16) | (*mtx >> 16);
	++mtx;
	*mtx = (*mtx << 24) | (*mtx >> 8);
}

inline void MixColumns(std::uint8_t* mtx) {
	std::uint8_t a, b, c, d;
	std::uint8_t* A, * B, * C, * D;
	for (int i = 0; i < 4; ++i, ++mtx) {
		a = *(A = mtx), b = *(B = mtx + 4), c = *(C = mtx + 8), d = *(D = mtx + 12);
		*A = Mul_02[a] ^ Mul_03[b] ^ c ^ d;
		*B = a ^ Mul_02[b] ^ Mul_03[c] ^ d;
		*C = a ^ b ^ Mul_02[c] ^ Mul_03[d];
		*D = Mul_03[a] ^ b ^ c ^ Mul_02[d];
	}
}

inline void InvMixColumns(std::uint8_t* mtx) {
	std::uint8_t a, b, c, d;
	std::uint8_t* A, * B, * C, * D;
	for (int i = 0; i < 4; ++i, ++mtx) {
		a = *(A = mtx), b = *(B = mtx + 4), c = *(C = mtx + 8), d = *(D = mtx + 12);
		*A = Mul_0e[a] ^ Mul_0b[b] ^ Mul_0d[c] ^ Mul_09[d];
		*B = Mul_09[a] ^ Mul_0e[b] ^ Mul_0b[c] ^ Mul_0d[d];
		*C = Mul_0d[a] ^ Mul_09[b] ^ Mul_0e[c] ^ Mul_0b[d];
		*D = Mul_0b[a] ^ Mul_0d[b] ^ Mul_09[c] ^ Mul_0e[d];
	}
}

inline void AddRoundKey(std::uint8_t* mtx, const std::uint8_t* key) {
	for (int i = 0; i < 4; ++i, ++mtx) {
		*mtx ^= *key++;
		*(mtx + 4) ^= *key++;
		*(mtx + 8) ^= *key++;
		*(mtx + 12) ^= *key++;
	}
}

inline void Det(std::uint8_t* dst, const std::uint8_t* src) {
	for (int i = 0; i < 4; ++i, ++dst) {
		*dst = *src++;
		*(dst + 4) = *src++;
		*(dst + 8) = *src++;
		*(dst + 12) = *src++;
	}
}

