inline void ecb_encrypt(std::uint8_t* out, const std::uint8_t* in, size_t in_size, const std::uint8_t* key, int Nr, std::uint8_t* end) {
	while (in_size >= BLOCK_SIZE) {
		encrypt(out, in, key, Nr);
		in += BLOCK_SIZE;
		out += BLOCK_SIZE;
		in_size -= BLOCK_SIZE;
	}

	if (end) {
		memcpy(end, in, in_size);
		encrypt(out, end, key, Nr);
	}
}

inline void ecb_decrypt(std::uint8_t* out, const std::uint8_t* in, size_t in_size, const std::uint8_t* key, int Nr) {
	while (in_size >= BLOCK_SIZE) {
		decrypt(out, in, key, Nr);
		in += BLOCK_SIZE;
		out += BLOCK_SIZE;
		in_size -= BLOCK_SIZE;
	}
}
