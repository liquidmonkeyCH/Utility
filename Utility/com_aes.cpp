// CBC MODE
inline void cbc_encrypt(std::uint8_t* out, const std::uint8_t* in, size_t in_size, const std::uint8_t* key, const std::uint8_t* iv, int Nr, std::uint8_t* end) {
	const std::uint8_t* p = iv;
	while (in_size >= BLOCK_SIZE) {
		memcpy(out, in, BLOCK_SIZE);
		Xor((std::uint32_t*)out, (std::uint32_t*)p);
		encrypt(out, out, key, Nr);
		p = out;
		in_size -= BLOCK_SIZE;
		in += BLOCK_SIZE;
		out += BLOCK_SIZE;
	}

	if (end) {
		memcpy(end, in, in_size);
		Xor((std::uint32_t*)end, (std::uint32_t*)p);
		encrypt(out, end, key, Nr);
	}
}

inline void cbc_decrypt(std::uint8_t* out, const std::uint8_t* in, size_t in_size, const std::uint8_t* key, const std::uint8_t* iv, int Nr) {
	const std::uint8_t* p = iv;
	while (in_size >= BLOCK_SIZE) {
		decrypt(out, in, key, Nr);
		Xor((std::uint32_t*)out, (std::uint32_t*)p);
		p = in;
		in += BLOCK_SIZE;
		out += BLOCK_SIZE;
		in_size -= BLOCK_SIZE;
	}
}
