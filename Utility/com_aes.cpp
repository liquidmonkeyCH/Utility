int aes_base::check_param(size_t& out_size, size_t& in_size, mode_t mode, padding_t& padding) {
	if (padding == padding_t::None) {
		if(mode == mode_t::CTR) {
			if (out_size < in_size) return Overflow;
			out_size = in_size;
			return Success;
		}

		if (mode == mode_t::GCM) {
			if (out_size < in_size + BLOCK_SIZE) return Overflow;
			out_size = in_size + BLOCK_SIZE;
			return Success;
		}

		if (in_size & SIZE_MASK) return NeedPadding;
		if (out_size < in_size) return Overflow;
	}

	unsigned char left = in_size & SIZE_MASK;
	unsigned char need = BLOCK_SIZE - left;
	if (out_size < in_size + need) return Overflow;
	out_size = in_size + need;
	if (mode == mode_t::GCM) out_size += BLOCK_SIZE;

	switch (padding)
	{
	case Utility::_impl::aes_base::padding_t::Zero:
		memset(m_end + left, 0, need);
		break;
	case Utility::_impl::aes_base::padding_t::Pkcs7:
		memset(m_end + left, need, need);
		break;
	case Utility::_impl::aes_base::padding_t::AnsiX923:
		memset(m_end + left, 0, need);
		m_end[aes_base::SIZE_MASK] = need;
		break;
	case Utility::_impl::aes_base::padding_t::iso10126:
		for (size_t i = left; i < aes_base::SIZE_MASK; ++i) m_end[i] = com::rand() & 0xFF;
		m_end[aes_base::SIZE_MASK] = need;
		break;
	default:
		break;
	}
	
	return Success;
}

int aes_base::do_encrypt(char* out, size_t& out_size, const char* in, size_t in_size, const std::uint32_t* key, mode_t mode, padding_t padding, int Nr){
	int res = check_param(out_size, in_size, mode, padding);
	if (res != 0) return res;
	switch (mode)
	{
	case Utility::_impl::aes_base::mode_t::ECB:
		ecb_encrypt((std::uint8_t*)out, (std::uint8_t*)in, in_size, (std::uint8_t*)key, Nr, padding == padding_t::None ? nullptr : m_end);
		break;
	case Utility::_impl::aes_base::mode_t::CBC:
		cbc_encrypt((std::uint8_t*)out, (std::uint8_t*)in, in_size, (std::uint8_t*)key, (std::uint8_t*)m_iv.m_data, Nr, padding == padding_t::None ? nullptr : m_end);
		break;
	case Utility::_impl::aes_base::mode_t::CTR:
		ctr_encrypt((std::uint8_t*)out, (std::uint8_t*)in, in_size, (std::uint8_t*)key, (std::uint8_t*)m_iv.m_data, Nr, padding == padding_t::None ? nullptr : m_end);
		break;
	case Utility::_impl::aes_base::mode_t::GCM:
		m_iv.m_len = GCM_IV_SIZE;
		gcm_encrypt((std::uint8_t*)out, (std::uint8_t*)in, in_size, (std::uint8_t*)key, (std::uint8_t*)m_iv.m_data, Nr, padding == padding_t::None ? nullptr : m_end, (std::uint8_t*)m_auth_ptr, m_auth_len);
		break;
	case Utility::_impl::aes_base::mode_t::CFB:
	case Utility::_impl::aes_base::mode_t::OFB:
	default:
		return BadMode;
	}

	return 0;
}

int aes_base::do_decrypt(char* out, size_t& out_size, const char* in, size_t in_size, const std::uint32_t* key, mode_t mod, padding_t padding, int Nr) {
	if (out_size < in_size) return Overflow;
	if (in_size & SIZE_MASK) return BadLen;
	out_size = in_size;

	switch (mod)
	{
	case Utility::_impl::aes_base::mode_t::ECB:
		ecb_decrypt((std::uint8_t*)out, (std::uint8_t*)in, in_size, (std::uint8_t*)key, Nr);
		break;
	case Utility::_impl::aes_base::mode_t::CBC:
		cbc_decrypt((std::uint8_t*)out, (std::uint8_t*)in, in_size, (std::uint8_t*)key, (std::uint8_t*)m_iv.m_data, Nr);
		break;
	case Utility::_impl::aes_base::mode_t::CTR:
		ctr_encrypt((std::uint8_t*)out, (std::uint8_t*)in, in_size, (std::uint8_t*)key, (std::uint8_t*)m_iv.m_data, Nr);
		break;
	case Utility::_impl::aes_base::mode_t::CFB:
	case Utility::_impl::aes_base::mode_t::OFB:
	default:
		return BadMode;
	}

	switch (padding)
	{
	case Utility::_impl::aes_base::padding_t::None:
		break;
	case Utility::_impl::aes_base::padding_t::Zero:
		out += out_size;
		while (!(*--out)) --out_size;
		break;
	case Utility::_impl::aes_base::padding_t::Pkcs7:
	case Utility::_impl::aes_base::padding_t::AnsiX923:
	case Utility::_impl::aes_base::padding_t::iso10126:
		out += out_size - 1;
		out_size -= *out;
		break;
	default:
		break;
	}

	return 0;
}
